angular
	.module('diputado',['ngRoute','ngSession','ngLocal'])
	.config(function($routeProvider){
	    $routeProvider
	    	.when('/',{
	    		templateUrl:'views/diputados.html',
	    		controller:'diputados'
	    	})
	    	.when('/diputados',{
	    		templateUrl:'views/diputados.html',
	    		controller:'diputados'
	    	})
		    .when('/:diputado',{
		        templateUrl:'views/diputado.html',
		        controller:'diputado'
		    })
		    .otherwise({redirectTo:'/diputados'});
	});